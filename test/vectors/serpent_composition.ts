export interface SerpentSealVector {
	description: string;
	key: string;          // hex, 64 bytes
	iv: string;           // hex, 16 bytes (injected via _iv seam)
	plaintext: string;    // hex
	ciphertext: string;   // hex (iv || cbc_ct || tag combined output)
	tag: string;          // hex, 32 bytes (last 32 bytes of ciphertext)
}

export interface ChunkVector {
	index: number;
	isLast: boolean;
	chunkInfo: string;    // hex, 54 bytes
	encKey: string;       // hex, 32 bytes (HKDF output[0..32])
	macKey: string;       // hex, 32 bytes (HKDF output[32..64])
	plaintext: string;    // hex (this chunk's plaintext slice)
	tag: string;          // hex, 32 bytes
}

export interface SerpentStreamVector {
	description: string;
	key: string;          // hex, 32 bytes
	streamNonce: string;  // hex, 16 bytes (injected via _nonce seam)
	plaintext: string;    // hex (full plaintext)
	chunkSize: number;
	chunkCount: number;
	chunks: ChunkVector[];
	output: string;       // hex (complete wire format output)
}

