//
// 1 P-256 SHA-256 group (tgId=14), 10 tests total. Non-P-256 curves,
// non-SHA-256 hashes, and SP 800-106 conformance groups (separate
// randomized-hashing construction) are filtered out at transcription
// time; this file covers only the baseline P-256 + SHA-256 records.
//
// FIPS 186-5 §6.4, ECDSA Signature Generation Algorithm. Every record
// carries its own per-record nonce `k`, which the verifier feeds into
// the explicit-k signing path (`ecdsa::hazmat::sign_prehashed`). The
// resulting signature MUST reproduce ACVP's (r, s) byte-for-byte; this
// gates the construction's r = x_R mod n and s = k^-1 * (z + r*d) mod n
// arithmetic without depending on RFC 6979 deterministic-k derivation
// (which is gated separately by ecdsa_p256.ts).
//
// d, qx, qy are shared across the testGroup in the ACVP shape; they
// are copied onto each per-test record here so the verifier does not
// need group-level lookups, mirroring the ed25519_siggen.ts convention.

export interface SigGenVector {
	tcId:    number;
	tgId:    number;
	testType: string;  // 'AFT'
	d:       string;   // 32 bytes (group-level scalar, copied per record)
	qx:      string;   // 32 bytes
	qy:      string;   // 32 bytes
	k:       string;   // 32 bytes (per-record explicit nonce, FIPS 186-5 §6.4.2)
	message: string;   // pre-prehash message bytes
	r:       string;   // 32 bytes
	s:       string;   // 32 bytes
}

