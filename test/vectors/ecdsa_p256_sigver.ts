// NIST ACVP ECDSA-FIPS186-5 sigVer test vectors (P-256, SHA-256 only).
//
// Source:
//   ACVP-Server/gen-val/json-files/ECDSA-SigVer-FIPS186-5/internalProjection.json
//   @see https://github.com/usnistgov/ACVP-Server/tree/master/gen-val/json-files/ECDSA-SigVer-FIPS186-5
//   algorithm=ECDSA, mode=sigVer, revision=FIPS186-5, isSample=true
//   ACVP-Server pin: 15c0f3deeefbfa8cb6cd32a99e1ca3b738c66bf0
//
// 1 P-256 SHA-256 group (tgId=8), 7 tests total. Non-P-256 curves and
// non-SHA-256 hashes are filtered out at transcription time.
//
// FIPS 186-5 §6.4.4, ECDSA Signature Verification Algorithm. The mixed
// pass/fail records exercise the verifier's rejection paths:
//   - 'modify r' / 'modify s' / 'modify message' / 'modify key': perturbations
//     of an otherwise-valid signature record. Verifier MUST return false.
//   - 'zero r' / 'zero s': boundary records targeting impls that accept
//     r=0 or s=0 (FIPS 186-5 §6.4.4 step 1 requires both in [1, n-1]).
//   - 'none' (testPassed=true): the well-formed positive control.
//
// d is the test-side witness scalar (the private key matching qx, qy);
// the verifier does not use d directly but it is surfaced for the
// per-record audit log so a maintainer can re-sign and confirm the
// ACVP-recorded sig was generated from this keypair.

export interface SigVerVector {
	tcId:       number;
	tgId:       number;
	testType:   string; // 'AFT'
	testPassed: boolean;
	reason:     string; // 'none' | 'modify r' | 'modify s' | 'zero r' | 'zero s' | 'modify message' | 'modify key'
	d:  string; // 32 bytes (test-side witness scalar, see header)
	qx: string; // 32 bytes
	qy: string; // 32 bytes
	message: string;
	r:  string; // 32 bytes (may be all-zero in 'zero r' records)
	s:  string; // 32 bytes (may be all-zero in 'zero s' records)
}

