// test/vectors/mldsa_siggen_kats.ts
//
// NIST ACVP ML-DSA SigGen rejection-path and high-rejection-count KATs.
//
// FIPS 204 Algorithm 7 ML-DSA.Sign_internal contains a rejection-sampling
// loop with four reject conditions: `‖z‖∞ ≥ γ₁ − β`, `‖r₀‖∞ ≥ γ₂ − β`,
// hint popcount > ω, and `‖ct₀‖∞ ≥ γ₂`. The ct₀ check is only reachable
// on ML-DSA-44; on -65 and -87 it cannot fire. These KATs exercise each
// path (Table 1) and stress implementations that abort early under heavy
// retry (Table 2, ≥ 32 rejections per vector).
//
// Source: ACVP ML-DSA JSON Specification §6.1.2 "ML-DSA SigGen Test Types",
// Tables 1 and 2.
//   Spec page:  https://pages.nist.gov/ACVP/draft-celi-acvp-ml-dsa.html
//   Raw source: https://github.com/usnistgov/ACVP/blob/master/src/ml-dsa/sections/04-testtypes.adoc
//
// The ML-DSA-65 and ML-DSA-87 Table-1 entries and the ML-DSA-65/-87 Table-2
// entries here reflect the Nov 19 2025 regeneration upstream commit
// `usnistgov/ACVP@f66d187` ("Fixes ML-DSA tables with test cases that
// cover what they are intended to cover"), which superseded the originally
// published vectors that did not cover all rejection paths. See the
// PQC-Forum thread that surfaced the issue:
// https://groups.google.com/a/list.nist.gov/g/pqc-forum/c/6U34L4ISYzk.
//
// Each record carries:
//   seed         32-byte KeyGen input ξ  (ML-DSA.KeyGen_internal, Alg 6).
//   keypairHash  SHA2-256(pk ‖ sk)        where (pk, sk) = KeyGen(ξ).
//   mPrime       raw M′ input to Sign_internal (Alg 7 line 6). The spec
//                packs M′ as 32 bytes per row.
//   sigHash      SHA2-256(σ)              where
//                σ = Sign_internal(sk, M′, rnd = 0³²).
//
// All KATs use deterministic signing (`rnd = 0³²`). The spec stores
// hashes of pk‖sk and σ rather than full bytes to save space (an
// ML-DSA-87 signature is 4627 bytes); the test harness reconstructs both
// halves and compares hashes.

export type ParameterSet = 'ML-DSA-44' | 'ML-DSA-65' | 'ML-DSA-87';

