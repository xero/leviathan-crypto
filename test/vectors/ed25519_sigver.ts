//   @see https://github.com/usnistgov/ACVP-Server/tree/master/gen-val/json-files/EDDSA-SigVer-1.0
//   algorithm=EDDSA, mode=sigVer, revision=1.0, isSample=true
//   ACVP-Server pin: 15c0f3deeefbfa8cb6cd32a99e1ca3b738c66bf0
//
// 2 ed25519 groups, 10 tests total (mixed pass/fail per
// the ACVP testPassed field). ed448 records are filtered out at
// transcription time.
//
// HashEdDSA corpus structure: same preHash discriminator convention as
// ed25519_siggen.ts. Each record carries testPassed (whether the
// signature should verify) and reason (failure cause for negative
// records, used in audit logs).
//
// Verification uses RFC 8032 §5.1.7 ('cofactored' strict path); the
// verifier oracle uses ed25519-dalek's verify_strict /
// verify_prehashed_strict, matching FIPS 186-5 §7.6.4 semantics.

export type PreHash = null | 'SHA-512';

export interface SigVerVector {
	tcId:       number;
	tgId:       number;
	testType:   string;   // 'AFT'
	testPassed: boolean;  // expected outcome of verification
	preHash:    PreHash;
	reason:     string;   // ACVP failure-cause string for negative records
	pk:         string;   // 32 bytes (q)
	sk:         string;   // 32 bytes (d), retained for diagnostic logs
	message:    string;
	context:    string;   // ctx bytes (may be empty)
	signature:  string;
}

