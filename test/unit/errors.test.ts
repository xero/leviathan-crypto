import { describe, it, expect } from 'vitest';
import { AuthenticationError } from '../../src/ts/errors.js';

describe('AuthenticationError', () => {
	it('instanceof AuthenticationError is true', () => {
		const e = new AuthenticationError('serpent');
		expect(e).toBeInstanceOf(AuthenticationError);
	});

	it('instanceof Error is true', () => {
		const e = new AuthenticationError('serpent');
		expect(e).toBeInstanceOf(Error);
	});

	it('message matches cipher name', () => {
		const e = new AuthenticationError('serpent');
		expect(e.message).toBe('serpent: authentication failed');
	});

	it('message includes cipher name for different ciphers', () => {
		expect(new AuthenticationError('chacha20-poly1305').message).toBe('chacha20-poly1305: authentication failed');
		expect(new AuthenticationError('xchacha20-poly1305').message).toBe('xchacha20-poly1305: authentication failed');
	});

	it('prototype chain is correct', () => {
		const e = new AuthenticationError('serpent');
		expect(Object.getPrototypeOf(e)).toBe(AuthenticationError.prototype);
	});
});
