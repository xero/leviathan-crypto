import { utf8ToBytes, wipe } from '../../utils.js';
import { SigningError } from '../../errors.js';
import {
	MlDsa44, MlDsa65, MlDsa87,
	MLDSA44, MLDSA65, MLDSA87,
} from '../../mldsa/index.js';
import type { MlDsaParams } from '../../mldsa/index.js';
import {
	buildEffectiveCtx,
	prehashAlgoToMldsa,
	CTX_DOMAIN_MAX,
} from '../ctx.js';
import type {
	SignatureSuite,
	StreamableSignatureSuite,
	PrehashAlgorithm,
} from '../types.js';

type MlDsaCtor = typeof MlDsa44 | typeof MlDsa65 | typeof MlDsa87;

// ‚ĒÄ‚ĒÄ Pure-mode factory ‚ĒÄ‚ĒÄ‚ĒÄ‚ĒÄ‚ĒÄ‚ĒÄ‚ĒÄ‚ĒÄ‚ĒÄ‚ĒÄ‚ĒÄ‚ĒÄ‚ĒÄ‚ĒÄ‚ĒÄ‚ĒÄ‚ĒÄ‚ĒÄ‚ĒÄ‚ĒÄ‚ĒÄ‚ĒÄ‚ĒÄ‚ĒÄ‚ĒÄ‚ĒÄ‚ĒÄ‚ĒÄ‚ĒÄ‚ĒÄ‚ĒÄ‚ĒÄ‚ĒÄ‚ĒÄ‚ĒÄ‚ĒÄ‚ĒÄ‚ĒÄ‚ĒÄ‚ĒÄ‚ĒÄ‚ĒÄ‚ĒÄ‚ĒÄ‚ĒÄ‚ĒÄ‚ĒÄ‚ĒÄ‚ĒÄ‚ĒÄ‚ĒÄ‚ĒÄ‚ĒÄ‚ĒÄ‚ĒÄ

function MldsaPureSuite(
	MlDsaClass: MlDsaCtor,
	params:     MlDsaParams,
	formatEnum: number,
	formatName: string,
	ctxDomain:  string,
): SignatureSuite {
	if (utf8ToBytes(ctxDomain).length > CTX_DOMAIN_MAX)
		throw new Error(
			`leviathan-crypto: ctxDomain '${ctxDomain}' too long for ${formatName}`,
		);

	const wasmModules = Object.freeze(['mldsa', 'sha3'] as const);

	return {
		formatEnum,
		formatName,
		ctxDomain,
		pkSize: params.pkBytes,
		skSize: params.skBytes,
		sigMaxSize: params.sigBytes,
		wasmModules,

		sign(sk: Uint8Array, msg: Uint8Array, ctx: Uint8Array): Uint8Array {
			const effectiveCtx = buildEffectiveCtx(ctxDomain, ctx);
			const inst = new MlDsaClass();
			try {
				return inst.sign(sk, msg, effectiveCtx);
			} finally {
				inst.dispose();
				wipe(effectiveCtx);
			}
		},

		verify(
			pk:  Uint8Array,
			msg: Uint8Array,
			sig: Uint8Array,
			ctx: Uint8Array,
		): boolean {
			const effectiveCtx = buildEffectiveCtx(ctxDomain, ctx);
			const inst = new MlDsaClass();
			try {
				return inst.verify(pk, msg, sig, effectiveCtx);
			} finally {
				inst.dispose();
				wipe(effectiveCtx);
			}
		},

		keygen(): { pk: Uint8Array; sk: Uint8Array } {
			const inst = new MlDsaClass();
			try {
				const kp = inst.keygen();
				return { pk: kp.verificationKey, sk: kp.signingKey };
			} finally {
				inst.dispose();
			}
		},
	};
}

// ‚ĒÄ‚ĒÄ Prehash-mode factory ‚ĒÄ‚ĒÄ‚ĒÄ‚ĒÄ‚ĒÄ‚ĒÄ‚ĒÄ‚ĒÄ‚ĒÄ‚ĒÄ‚ĒÄ‚ĒÄ‚ĒÄ‚ĒÄ‚ĒÄ‚ĒÄ‚ĒÄ‚ĒÄ‚ĒÄ‚ĒÄ‚ĒÄ‚ĒÄ‚ĒÄ‚ĒÄ‚ĒÄ‚ĒÄ‚ĒÄ‚ĒÄ‚ĒÄ‚ĒÄ‚ĒÄ‚ĒÄ‚ĒÄ‚ĒÄ‚ĒÄ‚ĒÄ‚ĒÄ‚ĒÄ‚ĒÄ‚ĒÄ‚ĒÄ‚ĒÄ‚ĒÄ‚ĒÄ‚ĒÄ‚ĒÄ‚ĒÄ‚ĒÄ‚ĒÄ‚ĒÄ‚ĒÄ‚ĒÄ

function MldsaPrehashSuite(
	MlDsaClass:       MlDsaCtor,
	params:           MlDsaParams,
	formatEnum:       number,
	formatName:       string,
	ctxDomain:        string,
	prehashAlgorithm: PrehashAlgorithm,
	prehashSize:      number,
): StreamableSignatureSuite {
	if (utf8ToBytes(ctxDomain).length > CTX_DOMAIN_MAX)
		throw new Error(
			`leviathan-crypto: ctxDomain '${ctxDomain}' too long for ${formatName}`,
		);

	const wasmModules   = Object.freeze(['mldsa', 'sha3'] as const);
	const mldsaHashAlgo = prehashAlgoToMldsa(prehashAlgorithm);

	return {
		formatEnum,
		formatName,
		ctxDomain,
		pkSize: params.pkBytes,
		skSize: params.skBytes,
		sigMaxSize: params.sigBytes,
		wasmModules,
		prehashAlgorithm,
		prehashSize,

		sign(sk: Uint8Array, msg: Uint8Array, ctx: Uint8Array): Uint8Array {
			const effectiveCtx = buildEffectiveCtx(ctxDomain, ctx);
			const inst = new MlDsaClass();
			try {
				return inst.signHash(sk, msg, mldsaHashAlgo, effectiveCtx);
			} finally {
				inst.dispose();
				wipe(effectiveCtx);
			}
		},

		verify(
			pk:  Uint8Array,
			msg: Uint8Array,
			sig: Uint8Array,
			ctx: Uint8Array,
		): boolean {
			const effectiveCtx = buildEffectiveCtx(ctxDomain, ctx);
			const inst = new MlDsaClass();
			try {
				return inst.verifyHash(pk, msg, sig, mldsaHashAlgo, effectiveCtx);
			} finally {
				inst.dispose();
				wipe(effectiveCtx);
			}
		},

		keygen(): { pk: Uint8Array; sk: Uint8Array } {
			const inst = new MlDsaClass();
			try {
				const kp = inst.keygen();
				return { pk: kp.verificationKey, sk: kp.signingKey };
			} finally {
				inst.dispose();
			}
		},

		signPrehashed(
			sk:     Uint8Array,
			digest: Uint8Array,
			ctx:    Uint8Array,
		): Uint8Array {
			if (digest.length !== prehashSize)
				throw new SigningError(
					'sig-malformed-input',
					`digest length ${digest.length} != expected ${prehashSize} for ${formatName}`,
				);
			const effectiveCtx = buildEffectiveCtx(ctxDomain, ctx);
			const inst = new MlDsaClass();
			try {
				return inst.signHashPrehashed(
					sk, digest, mldsaHashAlgo, effectiveCtx,
				);
			} finally {
				inst.dispose();
				wipe(effectiveCtx);
			}
		},

		verifyPrehashed(
			pk:     Uint8Array,
			digest: Uint8Array,
			sig:    Uint8Array,
			ctx:    Uint8Array,
		): boolean {
			if (digest.length !== prehashSize)
				throw new SigningError(
					'sig-malformed-input',
					`digest length ${digest.length} != expected ${prehashSize} for ${formatName}`,
				);
			const effectiveCtx = buildEffectiveCtx(ctxDomain, ctx);
			const inst = new MlDsaClass();
			try {
				return inst.verifyHashPrehashed(
					pk, digest, sig, mldsaHashAlgo, effectiveCtx,
				);
			} finally {
				inst.dispose();
				wipe(effectiveCtx);
			}
		},
	};
}

