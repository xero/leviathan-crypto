// FIPS 204 Algorithms 32 (ExpandA) and 33 (ExpandS) ‚ÄĒ pseudorandom
// expansion of ŌĀ to the matrix √ā and ŌĀ' to the noise polyvecs s‚āĀ, s‚āā.
//
// ExpandA samples √ā directly in NTT domain (RejNTTPoly produces NTT
// coefficients). ExpandS samples in time domain; the orchestration layer
// applies polyvec_ntt to s‚āĀ when needed for the matrix-vector product.

import type { MlDsaExports, Sha3Exports } from './types.js';
import type { MlDsaParams } from './params.js';
import { wipe } from '../utils.js';
import { shake128Squeezer, shake256Squeezer } from './sha3-helpers.js';

const POLY_BYTES = 1024;  // 256 √ó i32

/**
 * ExpandA ‚ÄĒ FIPS 204 Algorithm 32.
 *
 * For (i, j) ‚ąą [0, k) √ó [0, ‚Ąď):
 *   s ‚Üź ŌĀ ‚ÄĖ IntegerToBytes(j, 1) ‚ÄĖ IntegerToBytes(i, 1)
 *   √ā[i, j] ‚Üź RejNTTPoly(SHAKE128(s))
 *
 * Output is row-major: √ā[i, j] sits at matrixOff + (i¬∑‚Ąď + j) ¬∑ 1024.
 * This matches `polyvec_matrix_pointwise_montgomery`'s row-stride contract.
 *
 * ŌĀ is the public seed; the rej_ntt_poly inner loop has data-dependent
 * branching but only on ŌĀ-derived bytes (public), so no CT concern.
 */
export function expandA(
	mx: MlDsaExports,
	sx: Sha3Exports,
	params: MlDsaParams,
	rho: Uint8Array,
	matrixOff: number,
): void {
	const { k, l } = params;
	const xofPrfOff = mx.getXofPrfOffset();
	const mlMem = new Uint8Array(mx.memory.buffer);

	// XOF input: ŌĀ(32) ‚ÄĖ jByte ‚ÄĖ iByte (FIPS 204 ¬ß7.3 Alg 32 line 4)
	const xofSeed = new Uint8Array(34);
	xofSeed.set(rho, 0);

	for (let i = 0; i < k; i++) {
		for (let j = 0; j < l; j++) {
			xofSeed[32] = j;
			xofSeed[33] = i;
			const sq = shake128Squeezer(sx, xofSeed);
			const polyOff = matrixOff + (i * l + j) * POLY_BYTES;
			let ctr = 0;
			while (ctr < 256) {
				const block = sq.squeeze();
				mlMem.set(block, xofPrfOff);
				ctr += mx.rej_ntt_poly(polyOff, ctr, xofPrfOff, sq.rate);
			}
		}
	}
}

/**
 * ExpandS ‚ÄĒ FIPS 204 Algorithm 33.
 *
 * For r ‚ąą [0, ‚Ąď): s‚āĀ[r] ‚Üź RejBoundedPoly(SHAKE256(ŌĀ' ‚ÄĖ IntegerToBytes(r, 2)))
 * For r ‚ąą [0, k): s‚āā[r] ‚Üź RejBoundedPoly(SHAKE256(ŌĀ' ‚ÄĖ IntegerToBytes(r+‚Ąď, 2)))
 *
 * Note the index is 2 bytes (little-endian per FIPS 204 ¬ß7.1 Alg 11) ‚ÄĒ kyber
 * uses 1 byte because k ‚Č§ 4, but ML-DSA's max index is k+‚Ąď-1 = 14 (still
 * ‚Č§ 255 in practice but the spec mandates 2 bytes).
 *
 * ŌĀ' is secret. The local seed scratch is wiped on exit; the caller is
 * responsible for the WASM-resident ŌĀ' source buffer.
 */
export function expandS(
	mx: MlDsaExports,
	sx: Sha3Exports,
	params: MlDsaParams,
	rhoPrime: Uint8Array,
	s1Off: number,
	s2Off: number,
): void {
	const { k, l, eta } = params;
	const xofPrfOff = mx.getXofPrfOffset();
	const mlMem = new Uint8Array(mx.memory.buffer);

	// PRF input: ŌĀ'(64) ‚ÄĖ idx_lo ‚ÄĖ idx_hi
	const seed = new Uint8Array(66);
	seed.set(rhoPrime, 0);

	try {
		// s‚āĀ: ‚Ąď polynomials at indices 0..‚Ąď-1
		for (let r = 0; r < l; r++) {
			seed[64] =  r        & 0xFF;
			seed[65] = (r >>> 8) & 0xFF;
			const sq = shake256Squeezer(sx, seed);
			const off = s1Off + r * POLY_BYTES;
			let ctr = 0;
			while (ctr < 256) {
				const block = sq.squeeze();
				mlMem.set(block, xofPrfOff);
				ctr += mx.rej_bounded_poly(off, ctr, xofPrfOff, sq.rate, eta);
			}
		}
		// s‚āā: k polynomials at indices ‚Ąď..‚Ąď+k-1
		for (let r = 0; r < k; r++) {
			const idx = r + l;
			seed[64] =  idx        & 0xFF;
			seed[65] = (idx >>> 8) & 0xFF;
			const sq = shake256Squeezer(sx, seed);
			const off = s2Off + r * POLY_BYTES;
			let ctr = 0;
			while (ctr < 256) {
				const block = sq.squeeze();
				mlMem.set(block, xofPrfOff);
				ctr += mx.rej_bounded_poly(off, ctr, xofPrfOff, sq.rate, eta);
			}
		}
	} finally {
		// Local seed buffer carries ŌĀ' for the duration of this call; wipe
		// even on early throw so it never persists in TS heap.
		wipe(seed);
	}
}
