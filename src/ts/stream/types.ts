// CipherSuite interface ‚ÄĒ cipher-specific logic injected into SealStream
// and OpenStream. Implementations are plain objects (not classes).

export interface DerivedKeys {
	readonly bytes: Uint8Array;
}

export interface CipherSuite {
	readonly formatEnum: number;
	readonly hkdfInfo: string;
	readonly keySize: number;
	readonly tagSize: number;
	readonly padded: boolean;

	deriveKeys(masterKey: Uint8Array, nonce: Uint8Array): DerivedKeys;

	sealChunk(
		keys: DerivedKeys,
		counterNonce: Uint8Array,
		chunk: Uint8Array,
		aad?: Uint8Array,
	): Uint8Array;

	openChunk(
		keys: DerivedKeys,
		counterNonce: Uint8Array,
		chunk: Uint8Array,
		aad?: Uint8Array,
	): Uint8Array;

	wipeKeys(keys: DerivedKeys): void;

	readonly wasmModules: readonly string[];
	createPoolWorker(): Worker;
}

export interface SealStreamOpts {
	chunkSize?: number;
	framed?: boolean;
}
